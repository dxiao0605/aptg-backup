package aptg.cathaybkeco.vo;

public class PostCodeVO {
	
	private String postCode;
	private String city;
	private String dist;
	private String cityGroup;
	private String type;
	private String rankCode;
	private String areaCodeNo;
	
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDist() {
		return dist;
	}
	public void setDist(String dist) {
		this.dist = dist;
	}
	public String getCityGroup() {
		return cityGroup;
	}
	public void setCityGroup(String cityGroup) {
		this.cityGroup = cityGroup;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAreaCodeNo() {
		return areaCodeNo;
	}
	public void setAreaCodeNo(String areaCodeNo) {
		this.areaCodeNo = areaCodeNo;
	}
	public String getRankCode() {
		return rankCode;
	}
	public void setRankCode(String rankCode) {
		this.rankCode = rankCode;
	}
	
}
